def merge(c, r):
	if c[0] == r[0]:
		c[1] += r[1]
	elif r[1] > 0 and c[0] < r[0]:
		c[1] = r[1]
		c[0] = r[0]
	
def count(l, r, k, ii = None, ij = None):
	c = [k, 0]
	n = r - l + 1
	if k == 0:
		for i in range(1, n):
			for j in range(0, i):
				if (l + i) ^ (l + j) == 1:
					merge(c, count(l, r, 1, i, j))
		c[1] *= 4
		if c[1] == 0:
			c[1] = n
		return c
	else:
		if ii > 1 and (l + ii - 1) ^ (l + ij) == k + 1:
			merge(c, count(l, r, k + 1, ii - 1, ij))
		if ij > 0 and (l + ii) ^ (l + ij - 1) == k + 1:
			merge(c, count(l, r, k + 1, ii, ij - 1))
		if ii < n - 1 and (l + ii + 1) ^ (l + ij) == k + 1:
			merge(c, count(l, r, k + 1, ii + 1, ij))
		if ij < ii - 2 and (l + ii) ^ (l + ij + 1) == k + 1:
			merge(c, count(l, r, k + 1, ii, ij + 1))	
	if c[1] == 0:
		c[1] = 1
	return c		
		
